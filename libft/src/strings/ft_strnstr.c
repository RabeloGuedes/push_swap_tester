#include "../../inc/libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;
	size_t	j;

	i = 0;
	if (!*little)
		return ((char *)big);
	while (big[i] && len)
	{
		j = 0;
		while (big[i + j] == little[j] && big[i + j] && len > j)
		{
			if (!little[j + 1])
				return ((char *)&big[i]);
			j++;
		}
		i++;
		len--;
	}
	return (NULL);
}
